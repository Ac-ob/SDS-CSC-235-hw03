//learned to do counts with https://github.com/d3/d3-array/blob/v3.2.1/README.md#rollup

let frequency_data = Array.from(d3.rollup(myData, v=> v.length, d => d.family), ([key, value]) => ({key, value}))

let height = 300;
let width = 500;
let margin = 50;

let frame = d3.select("#bar_vis")
                .append("svg")
                .attr("width", width + margin*2)
                .attr("height", height + margin*2);

let linearScale = d3.scaleLinear()
                        .domain([0, 18])
                        .range([height - margin, margin]); 

frame.append("g")
        .attr("transform", `translate(${margin}, 0)`)
        .call(d3.axisLeft(linearScale)); 

let bandScale = d3.scaleBand()
                    .domain(frequency_data.map(function(d) { return d.key; }))
                    .range([margin, width-margin])
                    .paddingInner(0.05);

frame.append("g")
        .attr("transform", `translate(0, ${height-margin})`)
        .call(d3.axisBottom(bandScale))
        .selectAll("text")
        .attr("transform", "translate(-10,0)rotate(-45)")
        .style("text-anchor", "end");



//how to add color to single elements on click https://www.d3indepth.com/selections/
//help with if statement structure to make color appear and disapear - https://stackoverflow.com/a/66241453 // Posted by Raycas 




frame.selectAll("rect")
    .data(frequency_data)
    .join("rect")
    .attr("x", function(d, i) {
        return bandScale(d.key); 
    })
    .on('click', color_change)
    .attr("y", function(d, i) {
        return (linearScale(d.value));
    })
    .attr("width", bandScale.bandwidth())
    .attr("height", function(d, i) {
        return ((height - margin) -linearScale(d.value));
    } ); 

 function color_change(){
    let rectangle = d3.select(this);
      if(rectangle.style('fill') === 'black'){
      rectangle.style('fill',  'pink');
      } else {
        rectangle.style('fill',  'black');
  }
 }

